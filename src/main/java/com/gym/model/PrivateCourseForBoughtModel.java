package com.gym.model;

public class PrivateCourseForBoughtModel {
	private String privateCourseName;
	private String trainerName;
	private String memberName;
	private String buyPrivateCourseTime;
	private int buyPrivateCourseNumber;
	private float buyPrivateCoursePrice;
	public String getPrivateCourseName() {
		return privateCourseName;
	}
	public void setPrivateCourseName(String privateCourseName) {
		this.privateCourseName = privateCourseName;
	}
	public String getTrainerName() {
		return trainerName;
	}
	public void setTrainerName(String trainerName) {
		this.trainerName = trainerName;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getBuyPrivateCourseTime() {
		return buyPrivateCourseTime;
	}
	public void setBuyPrivateCourseTime(String buyPrivateCourseTime) {
		this.buyPrivateCourseTime = buyPrivateCourseTime;
	}
	public int getBuyPrivateCourseNumber() {
		return buyPrivateCourseNumber;
	}
	public void setBuyPrivateCourseNumber(int buyPrivateCourseNumber) {
		this.buyPrivateCourseNumber = buyPrivateCourseNumber;
	}
	public float getBuyPrivateCoursePrice() {
		return buyPrivateCoursePrice;
	}
	public void setBuyPrivateCoursePrice(float buyPrivateCoursePrice) {
		this.buyPrivateCoursePrice = buyPrivateCoursePrice;
	}
}
